
# (start.stop,step size)

for i in range (8):   # (8) by default means (0,8), or , starts from 0... till 8th number, i.e 7
    print(i)
for k in range (1,8):   # it starts from 1... till 8th number, i.e 8
    print(k)
# step size is the 'd' value as in arithmetic progression (by default 1 (in continuation)) 
for i in range (1,8,2):   # (8) by default means (0,8), or , starts from 0... till 8th number, i.e 7
    print(i)